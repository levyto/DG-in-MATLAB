classdef ScalarInviscidBurgers1D

  properties

    name = "1D Scalar Invicid Burgers Equation";
  
  end
   
  methods
    
    function f = EvalMaxEigenValue(obj, u)
      f = abs(u);
    end

    function f = EvalConvFlux(obj, u)
      f = 0.5 * u * u;
    end

    function f = Name(obj)
      f = obj.name;
    end

  end

