% Description: Linear Scalar Advection Equation                                %
%              u_t + (f(u))_x = 0                                              %
%              f(u) = au                                                       %
%                                                                              %
% ---------------------------------------------------------------------------- %
classdef ScalarLinearAdvection1D

  properties

    a = 0;
    name = "1D Scalar Linear Advection Equation";
  
  end
   
  methods

    function obj = ScalarLinearAdvection1D(val)
      if nargin == 1
        obj.a = val;
      end
    end

    function f = EvalMaxEigenValue(obj, u)
      f = abs(obj.a);
    end

    function f = EvalConvFlux(obj, u)
      f = obj.a * u;
    end

    function f = Name(obj)
      f = obj.name;
    end

  end

end