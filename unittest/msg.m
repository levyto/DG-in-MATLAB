function msg(correct, ut_name, fun_info)

  if nargin > 2
	  fun_name = fun_info.name;
    fprintf("\n %s.m\n", fun_name);
  end

  N = 80 - length(ut_name) - 3 - 3 - 1 - 1;
  whitespace = repmat(".",1,N);

  fprintf("   %s%s %d", ut_name, whitespace, correct);

	if correct
    fprintf("\n");
  else
    fprintf("   ERROR: UnitTest failed\n\n");
  end

end